function t(key){
  return (I18N[CURRENT_LANGUAGE] && I18N[CURRENT_LANGUAGE][key]) || I18N.en[key] || key;
}

function applyStaticTranslations(root=document){
  root.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });

  root.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    el.innerHTML = t(key);
  });

  root.querySelectorAll('[data-i18n-attr]').forEach(el => {
    const pairs = el.getAttribute('data-i18n-attr').split(';').map(s => s.trim()).filter(Boolean);
    pairs.forEach(pair => {
      const [attr, key] = pair.split(':').map(s => s.trim());
      if(attr && key) el.setAttribute(attr, t(key));
    });
  });

  document.documentElement.lang = CURRENT_LANGUAGE;
  document.title = t('page.title');
}

function validateTranslations(){
  const baseKeys = Object.keys(I18N.en);
  Object.entries(I18N).forEach(([lang, dict]) => {
    const missing = baseKeys.filter(key => !(key in dict));
    if(missing.length) console.warn(`Missing translations for ${lang}:`, missing);
  });
}

function detectBrowserLanguage(){
  const browserLang = (navigator.language || navigator.userLanguage || 'en').toLowerCase();
  const short = browserLang.split('-')[0];
  return SUPPORTED_LANGUAGES.includes(short) ? short : DEFAULT_LANGUAGE;
}

function getSavedLanguage(){
  try {
    return localStorage.getItem('language');
  } catch(e){
    return null;
  }
}

function saveLanguage(lang){
  try {
    localStorage.setItem('language', lang);
  } catch(e){}
}

function initializeLanguage(){
  const saved = getSavedLanguage();
  CURRENT_LANGUAGE = (saved && SUPPORTED_LANGUAGES.includes(saved))
    ? saved
    : detectBrowserLanguage();

  const sel = document.getElementById('languageSelect');
  if(sel) sel.value = CURRENT_LANGUAGE;

  applyStaticTranslations();
}

function setLanguage(lang){
  if(!SUPPORTED_LANGUAGES.includes(lang)) lang = DEFAULT_LANGUAGE;

  CURRENT_LANGUAGE = lang;
  saveLanguage(lang);
  applyStaticTranslations();

  const sel = document.getElementById('languageSelect');
  if(sel) sel.value = lang;

  if(typeof updateUnitLabels === 'function') updateUnitLabels();
  if(typeof recalc === 'function') recalc();
  if(typeof populateSizeFilterPanel === 'function') populateSizeFilterPanel();
  if(typeof updateSizeFilterSummary === 'function') updateSizeFilterSummary();
}
